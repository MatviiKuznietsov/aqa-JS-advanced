console.log('for example');
const numberMultiplication = 4;
for (let i = 1; i <= 10; i++) {
  console.log(`${numberMultiplication}*${i}=${numberMultiplication * i}`);
}
console.log('while example');
let index = 1;
while (index <= 10) {
  console.log(`${numberMultiplication}*${index}=${numberMultiplication * index}`);
  index++;
}
